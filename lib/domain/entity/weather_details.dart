class WeatherDetails {
  late final int dt;
  late final Temperature temperature;
  late final List<Weather> weather;
  late final Clouds clouds;
  late final Wind wind;
  late final int visibility;
  late final int? pop;
  late final int? timezone;
  late final String? name;
  late final String? dtTxt;

  WeatherDetails.fromJson(Map<String, dynamic> json) {
    dt = json['dt'];
    temperature = Temperature.fromJson(json['main']);
    weather = List.from(json['weather']).map((e) => Weather.fromJson(e)).toList();
    clouds = Clouds.fromJson(json['clouds']);
    wind = Wind.fromJson(json['wind']);
    visibility = json['visibility'];
    pop = json['pop'];
    timezone = json['timezone'];
    name = json['name'];
    dtTxt = json['dt_txt'];
  }

  Map<String, dynamic> toJson() {
    final data = <String, dynamic>{};
    data['dt'] = dt;
    data['main'] = temperature.toJson();
    data['weather'] = weather.map((e) => e.toJson()).toList();
    data['clouds'] = clouds.toJson();
    data['wind'] = wind.toJson();
    data['visibility'] = visibility;
    data['pop'] = pop;
    data['timezone'] = timezone;
    data['name'] = name;
    data['dt_txt'] = dtTxt;
    return data;
  }
}

class Weather {
  int? id;
  String? main;
  String? description;
  String? icon;

  Weather({this.id, this.main, this.description, this.icon});

  Weather.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    main = json['main'];
    description = json['description'];
    icon = json['icon'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['id'] = id;
    data['main'] = main;
    data['description'] = description;
    data['icon'] = icon;
    return data;
  }
}

class Temperature {
